export const UI: Record<Locale, UiDict> = { zh, en, ja };

function getNested(dict: UiDict, path: string): string | undefined {
  const parts = path.split(".");
  let cur: string | UiDict | undefined = dict;
  for (const p of parts) {
    if (typeof cur !== "object" || cur === null) return undefined;
    cur = cur[p] as string | UiDict | undefined;
  }
  return typeof cur === "string" ? cur : undefined;
}

export function t(
  locale: Locale,
  key: string,
  params?: Record<string, string>
): string {
  let text = getNested(UI[locale], key) ?? getNested(UI.zh, key) ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replace(new RegExp(`\\{${k}\\}`, "g"), v);
    }
  }
  return text;
}

export function tf(locale: Locale, zhKey: string): string {
  return t(locale, `fact.${zhKey}`, undefined) || zhKey;
}
